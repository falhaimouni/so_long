#include "libft.h"

int	ft_putstr_fd(char const *s, int fd)
{
	int	i;

	if (s)
	{
		i = 0;
		while (s[i])
		{
			write(fd, &s[i], 1);
			i++;
		}
		return (ft_strlen(s));
	}
	else
		return (write (fd, "(null)", 6));
}
